package jiyun.com.lovepet.ui.wallet.activity;

/**
 * 这个世界上没有天才和大神,只有不努力的笨蛋和菜鸟   ____刘荣斌_____
 */
public class Student {
 private String image;
 private String name;

    public Student(String image, String name) {
        this.image = image;
        this.name = name;
    }

    public Student() {
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
